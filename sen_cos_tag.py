import math
ang= float(input('Digite um 창ngulo qualquer: '))
seno = math.sin(math.radians(ang))
print('O 창ngulo de {} tem o SENO de {:.2f}' .format(ang, seno))
cos = math.cos(math.radians(ang))
print('O 창ngulo de {} tem o SENO de {:.2f}' .format(ang, cos))
tan = math.tan(math.radians(ang))
print('O 창ngulo de {} tem o SENO de {:.2f}' .format(ang, tan))